package account;

import goods.Items;

public class CartItem {  
    private Items items;  
  
    private int count;  
  
    public int getCount() {  
        return count;  
    }  
  
    public void setCount(int count) {  
        this.count = count;  
    }  
  
    public Items getitems() {  
        return items;  
    }  
  
    public void setitems(Items items) {  
        this.items = items;  
    }  
}  
